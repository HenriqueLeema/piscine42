#include <stdio.h>
#include <unistd.h>
#include <stdlib.h>

char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] = str[i] + 32;
		}
		i++;
	}
	return (str);
}

int main()
{
    char str1[] = "ja fui MAIUSCULA"; // full conversion into lowercase

    if (ft_strupcase(str1))
    {
        printf("%s is fully converted to lowercase\n", str1);
    }
   
    return 0;
}